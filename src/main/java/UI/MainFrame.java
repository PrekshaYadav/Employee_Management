    private void bt_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_loginActionPerformed
        // TODO add your handling code here:
        String role = comboBoxLogin.getSelectedItem().toString();
        String username = txt_username.getText();
        String password = txt_password.getText();
        if(role.equals("System Admin"))
        {
            if(username.equals("sysadmin") && password.equals("password"))
            {
            dispose();
            SystemAdminForm systemAdminForm = new SystemAdminForm(  patientDirectory, cityDirectory);
            systemAdminForm.show();
            }
        }
        
        else if(role.equals("Hospital Admin"))
        {
            
            if(username.equals("hosadmin") && password.equals("password"))
            {
            dispose();
            HospitalAdminForm hospitalAdmin = new HospitalAdminForm(hospitalDirectory, doctorDirectory, patientDirectory, personDirectory, cityDirectory);
            hospitalAdmin.show();
        }
        }
        else if(role.equals("Community Admin"))
        {
            
            if(username.equals("comadmin") && password.equals("password"))
            {
            dispose();
            CommunityAdminForm hospitalAdmin = new CommunityAdminForm(hospitalDirectory, doctorDirectory, patientDirectory, personDirectory, cityDirectory);
            hospitalAdmin.show();
            }
        }
        else if(role.equals("Doctor"))
        {
            int flag = 0;
            for(City city: cityDirectory.getCityDirectory())
            {
                for(Community com: city.getCommunityDir())
                {
                    for(Hospital hosp: com.getHospitalDirectory().getHospitalDirectory())
                    {
                        for(Doctor doc : hosp.getDocterDirector().getDocterDirectory())
                        {
                            if(doc.getUsername().equals(username) && doc.getPassword().equals(password))
                            {
                                dispose();
                                DocterAdminFrame doctorAdmin = new DocterAdminFrame(hospitalDirectory, doctorDirectory, patientDirectory, personDirectory, cityDirectory, doc);
                                doctorAdmin.show();
                                flag =1;
                                break;
                            }
                        }
                        
                    }
                }
            }
            if(flag == 0)
              JOptionPane.showMessageDialog(this, "Invalid credentials");  
            
            /*if(username.equals("docadmin") && password.equals("password"))
            {
            dispose();
            DocterAdminFrame doctorAdmin = new DocterAdminFrame(hospitalDirectory, doctorDirectory, patientDirectory, personDirectory, cityDirectory);
            doctorAdmin.show();
            }*/
        }
        
        else if(role.equals("Patient"))
        {
            
            if(username.equals("patadmin") && password.equals("password"))
            {
            dispose();
            PatientForm patient = new PatientForm();
            patient.show();
            }
        }
        
        //dispose();
        //SystemAdminForm systemAdminForm = new SystemAdminForm();
        //systemAdminForm.show();
    }//GEN-LAST:event_bt_loginActionPerformed

